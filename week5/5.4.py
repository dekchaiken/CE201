age = 13
if age >= 65:
    print('the person is a elder')
elif 65 > age >= 20:
    print('the person is a adult')
elif 20 > age >= 13:
    print('the person is a teenager')
elif 13 > age >= 4:
    print('the person is a kid')
elif 4 > age >= 2:
    print('the person is a toddler')
else:
    print ('the person is a baby')
